package org.oep.cmon.portlet.doanhnghiep.quanlytaikhoan.utils;

public class DanhSachQuanLyTKModel {
	private String primaryKey;
	private String maGiayPhep;
	private String email;
	private String ten;
	private String diaChi;
	private String doiTuong;
	private String trangThai;
	
	public String getPrimaryKey() {
		return primaryKey;
	}
	public void setPrimaryKey(String primaryKey) {
		this.primaryKey = primaryKey;
	}
	public String getMaGiayPhep() {
		return maGiayPhep;
	}
	public void setMaGiayPhep(String maGiayPhep) {
		this.maGiayPhep = maGiayPhep;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public String getDiaChi() {
		return diaChi;
	}
	public void setDiaChi(String diaChi) {
		this.diaChi = diaChi;
	}
	public String getDoiTuong() {
		return doiTuong;
	}
	public void setDoiTuong(String doiTuong) {
		this.doiTuong = doiTuong;
	}
	public String getTrangThai() {
		return trangThai;
	}
	public void setTrangThai(String trangThai) {
		this.trangThai = trangThai;
	}
	

}
